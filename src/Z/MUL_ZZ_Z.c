/*
  MUL_ZZ_Z

  Умножение двух целых чисел.
  Алгоритм:
    1) Определяем знаки множителей через POZ_Z_D.
    2) Если один из множителей равен нулю — возвращаем ноль.
    3) Перемножаем абсолютные величины через MUL_NN_N.
    4) Знак результата: если знаки одинаковые — плюс, разные — минус
       (определяется через MUL_ZM_Z при необходимости).

  Параметры:
    1) NUMBZ* a - первый множитель
    2) NUMBZ* b - второй множитель

  Возвращает указатель на новое NUMBZ = a * b
*/
NUMBZ* MUL_ZZ_Z(NUMBZ* a, NUMBZ* b) {
    NUMBZ* result = (NUMBZ*)malloc(sizeof(NUMBZ));

    int signA = POZ_Z_D(a);
    int signB = POZ_Z_D(b);

    /* если один из множителей ноль — результат ноль */
    if (signA == 0 || signB == 0) {
        result->b = 0;
        result->n = 1;
        result->A = allocDigits(1);
        return result;
    }

    /* перемножаем абсолютные величины */
    int aN, bN;
    int *aA, *bA;
    ABS_Z_N(a, &aN, &aA);
    ABS_Z_N(b, &bN, &bA);

    int resN;
    int* resA;
    MUL_NN_N(aA, aN, bA, bN, &resN, &resA);
    free(aA);
    free(bA);

    result->n = resN;
    result->A = resA;

    /* знак результата: одинаковые знаки → плюс, разные → минус */
    if (signA == signB) {
        result->b = 0; /* плюс */
    } else {
        result->b = 1; /* минус */
    }

    return result;
}
