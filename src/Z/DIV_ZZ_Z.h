#ifndef _DIV_ZZ_Z_H_
#define _DIV_ZZ_Z_H_

#include "numbZ.h"

/*
  DIV_ZZ_Z

  Частное от деления целого на целое (делитель отличен от нуля).
  Реализует математическое целочисленное деление с округлением в сторону
  минус бесконечности: если знаки делимого и делителя различны и деление
  нацело не получается, результат уменьшается на 1.

  Параметры:
    1) NUMBZ* a — делимое (целое число)
    2) NUMBZ* b — делитель (целое число, отличное от нуля)

  Возвращает: NUMBZ* — частное a / b, или NULL при ошибке.
*/
NUMBZ* DIV_ZZ_Z(NUMBZ* a, NUMBZ* b);

#endif