/*
  COM_NN_D

  Сравнение двух натуральных чисел: сначала по количеству цифр,
  затем поцифрово от старшего разряда к младшему.

  Примечание по структуре NUMBN:
    - A[0]   — старшая цифра числа
    - A[n-1] — младшая цифра числа
    - n      — количество цифр в числе

  Параметры:
    1) NUMBN* a - первое натуральное число
    2) NUMBN* b - второе натуральное число

  Возвращает int: 2 - a > b, 0 - a == b, 1 - a < b
*/
int COM_NN_D(NUMBN* a, NUMBN* b) {
    if (a->n != b->n) {
        return (a->n > b->n) ? 2 : 1;
    }

    for (int i = 0; i < a->n; i++) {
        if (a->A[i] != b->A[i]) {
            return (a->A[i] > b->A[i]) ? 2 : 1;
        }
    }

    return 0;
}
